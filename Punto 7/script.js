let dia = prompt("Escriba el nombre de algún día de la semana, no use mayúsculas:").toUpperCase();
if(dia === "LUNES"){
    alert("Hoy es un buen día para sonreír, feliz Lunes");
} else if (dia === "VIERNES"){
    alert("¡Es Viernes y el cuerpo lo sabe!");
} else if (dia === "SABADO"){
    alert("¡Sabadito para darte un gustico!");
} else if (dia === "DOMINGO"){
    alert("¡Un Domingo bien invertido trae consigo una semana llena de felicidad!");
}else{
    alert("¡Ponle una sonrisa a la vida!")
}